def semantic_chunking(text, chunk_size=500):
    sentences = sent_tokenize(text)

    chunks = []
    current_chunk = ""

    for sentence in sentences:

        # If sentence fits in current chunk
        if len(current_chunk) + len(sentence) <= chunk_size:
            current_chunk += " " + sentence

        else:
            # Save current chunk
            chunks.append(current_chunk.strip())

            # Start new chunk
            current_chunk = sentence

    # Add last chunk
    if current_chunk:
        chunks.append(current_chunk.strip())

    return chunks

# Split into chunks
def split_text(documents):

    chunks = []

    for doc in documents:

        # Semantic chunking
        split_chunks = semantic_chunking(doc["text"])

        for chunk in split_chunks:
            chunks.append({
                "text": chunk,
                "source": doc["source"]
            })

    return chunks


